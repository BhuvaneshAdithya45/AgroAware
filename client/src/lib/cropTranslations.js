// Function to translate a crop name
export function translateCrop(cropName, lang) {
    if (!cropName) return cropName;
    const key = cropName.toLowerCase().replace(/\s+/g, '');
    const translation = cropTranslations[key];
    if (translation && translation[lang]) {
        return translation[lang];
    }
    return cropName; // Return original if no translation found
}

// Function to translate an array of crop names
export function translateCrops(crops, lang) {
    if (!Array.isArray(crops)) return crops;
    return crops.map(crop => translateCrop(crop, lang));
}
